#include<iostream>
using namespace std;
int main()
{
	int a=2,b=5,add,sub,mul,div,modulo;;
	add=a+b;
	sub=a-b;
	mul=a*b;
	div=a/b;
	modulo=a%b;
	cout<<"addition:"<<add<<endl;
	cout<<"subtraction:"<<sub<<endl;
	cout<<"multiplication:"<<mul<<endl;
	cout<<"division:"<<div<<endl;
	cout<<"modulo:"<<modulo<<endl;
}
